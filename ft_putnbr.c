#include <unistd.h>

void	ft_putnbr(int n)
{
	char	c;

	if (n > 9 || n < -9)
		ft_putnbr(n / 10);
	else if (n < 0)
		write(1, "-", 1);
	c = '0' + n % 10 * ((n > 0) - (n < 0));
	write(1, &c, 1);
}
